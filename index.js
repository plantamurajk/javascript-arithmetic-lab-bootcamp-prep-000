function add(x, y){
  return (x + y)
}

function subtract(x, y){
  return(x - y)
}

function multiply(x, y){
  return (x * y)
}

function divide(x, y){
  return (x / y)
}

function inc(n){
  n += 1
  return n
}

function dec(n){
  n -= 1
  return n
}

function makeInt(n){
  parseInt(n, `10`)
  return n
}